  /**
   * AI-enhanced: Parse natural language and generate appropriate Excel content
   * Now uses AI to understand complex requests and generate advanced formulas
   */
  async parseUserRequest(userQuery: string, uploadedData?: any[][]): Promise<SpreadsheetRequest> {
    const request: SpreadsheetRequest = {
      operation: 'create',
      data: uploadedData,
      tables: [],
      formulas: [],
      calculations: []
    };

    // Use AI to analyze user intent and detect required formulas/models
    const queryLower = userQuery.toLowerCase();
    
    // Detect financial model requests
    if (queryLower.match(/dcf|discounted cash flow|valuation/i)) {
      request.calculations?.push({
        type: 'custom',
        inputs: { modelType: 'dcf' },
        outputLocation: 'A1'
      });
    }

    if (queryLower.match(/lbo|leveraged buyout/i)) {
      request.calculations?.push({
        type: 'custom',
        inputs: { modelType: 'lbo' },
        outputLocation: 'A1'
      });
    }

    if (queryLower.match(/3-?statement|three statement/i)) {
      request.calculations?.push({
        type: 'custom',
        inputs: { modelType: '3-statement' },
        outputLocation: 'A1'
      });
    }

    // Detect formula generation requests
    if (queryLower.match(/xlookup|vlookup|lookup/i)) {
      request.formulas?.push({
        cell: 'B2',
        formula: '', // Will be generated by AI
        description: 'Lookup formula'
      });
    }

    if (queryLower.match(/index.*match|index-match/i)) {
      request.formulas?.push({
        cell: 'B2',
        formula: '', // Will be generated by AI
        description: 'INDEX-MATCH formula'
      });
    }

    if (queryLower.match(/sumifs?|countifs?|averageifs?/i)) {
      request.formulas?.push({
        cell: 'B2',
        formula: '', // Will be generated by AI
        description: 'Conditional aggregation formula'
      });
    }

    // Extract calculation requirements (existing logic)
    if (queryLower.includes('tax')) {
      request.calculations?.push({
        type: 'tax',
        inputs: {},
        outputLocation: 'B10'
      });
    }

    if (queryLower.match(/npv|net present value/i)) {
      request.calculations?.push({
        type: 'npv',
        inputs: {},
        outputLocation: 'B15'
      });
    }

    if (queryLower.match(/irr|internal rate of return/i)) {
      request.calculations?.push({
        type: 'irr',
        inputs: {},
        outputLocation: 'B20'
      });
    }

    return request;
  }

  /**
   * Generate complete financial model from AI prompt
   */
  async generateFinancialModel(modelType: string, params: any): Promise<ExcelOperationResult> {
    let workbook: ExcelJS.Workbook;

    switch (modelType.toLowerCase()) {
      case 'dcf':
        workbook = await financialModelTemplates.generateDCFModel({
          companyName: params.companyName || 'Company',
          forecastYears: params.years || 5,
          wacc: params.wacc,
          terminalGrowth: params.terminalGrowth
        });
        break;
      
      case '3-statement':
        workbook = await financialModelTemplates.generate3StatementModel({
          companyName: params.companyName || 'Company',
          years: params.years || 5
        });
        break;
      
      case 'budget':
        workbook = await financialModelTemplates.generateBudgetTemplate({
          businessName: params.businessName || 'Business',
          fiscalYear: params.year || new Date().getFullYear(),
          departments: params.departments
        });
        break;
      
      default:
        throw new Error(`Unknown model type: ${modelType}`);
    }

    const buffer = await workbook.xlsx.writeBuffer();

    return {
      workbook,
      buffer: Buffer.from(buffer),
      summary: `${modelType.toUpperCase()} model generated with complete formulas and structure`,
      formulasUsed: [],
      tablesCreated: workbook.worksheets.map(ws => ws.name)
    };
  }

