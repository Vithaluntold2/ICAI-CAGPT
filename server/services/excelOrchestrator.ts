  /**
   * Apply standard formatting - Professional styling
   */
  private applyStandardFormatting(sheet: ExcelJS.Worksheet, formatting?: FormattingOptions) {
    // Set default font for entire sheet
    sheet.eachRow((row) => {
      row.eachCell((cell) => {
        if (!cell.font || !cell.font.name) {
          cell.font = { ...cell.font, name: 'Calibri', size: cell.font?.size || 11 };
        }
      });
    });

    // Auto-fit columns with better defaults
    sheet.columns.forEach((column, idx) => {
      if (column && column.values) {
        let maxLength = 12; // Minimum width
        column.values.forEach((value) => {
          if (value) {
            const length = value.toString().length;
            if (length > maxLength) maxLength = length;
          }
        });
        column.width = Math.min(maxLength + 4, 40);
      }
    });

    // Add print settings
    sheet.pageSetup = {
      orientation: 'landscape',
      fitToPage: true,
      fitToWidth: 1,
      fitToHeight: 0,
      margins: {
        left: 0.5,
        right: 0.5,
        top: 0.75,
        bottom: 0.75,
        header: 0.3,
        footer: 0.3
      }
    };

    // Freeze top row
    sheet.views = [{ state: 'frozen', ySplit: 1 }];
  }

